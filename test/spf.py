from typing import List



def spf(graph: dict, stt: list, end: list, lastCost=0):  # -> List[List[int]]:
    # get stt up
    if str(stt) == str(end):
        return [stt]
    if str(stt) not in graph.keys():
        return None

    nextHops = graph[str(stt)]
    for nextHop in nextHops:
        path = spf(graph, nextHop, end, lastCost + 1)
        if path is not None:
            return [stt] + path


